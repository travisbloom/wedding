const getPathName = (path: string) => path.split('/')[1] || 'home';

class AnimatedLogoComponent extends React.Component<RouteComponentProps<any>, { prevPath: string | null }> {
  state = {
    prevPath: '',
  };

  componentWillReceiveProps() {
    this.setState({ prevPath: getPathName(this.props.location.pathname) });
  }
  render() {
    const { location } = this.props;
    const pathSVG = paths[getPathName(this.props.location.pathname)];
    const interpolator = interpolate(this.state.prevPath ? paths[this.state.prevPath] : pathSVG, pathSVG, {
      maxSegmentLength: 0.1,
    });
    return (
      <svg width="30" height="20">
        <g
          id="svgGroup"
          strokeLinecap="round"
          fillRule="evenodd"
          fontSize="9pt"
          stroke="#fff"
          fill="none"
          style={{ stroke: '#fff', fill: 'none' }}
        >
          <Spring reset={true} native={true} from={{ t: 0 }} to={{ t: 1 }}>
            {({ t }) => <animated.path d={t.interpolate(interpolator)} />}
          </Spring>
        </g>
      </svg>
    );
  }
}

export const AnimatedLogo = withRouter(AnimatedLogoComponent);
